    public void alterarMinutero(long microsec) {
        Integer hour, min, sec;
        //Calculo del microsegundo
        sec = (int) microsec / 1000000;
        //Calculo del minutero
        min = sec / 60;
        sec = sec - min * 60;
        hour = min / 60;
        min = min - hour * 60;
        String shour, smin, ssec;
        if (sec < 10) {
            ssec = "0" + sec;
        } else {
            ssec = sec.toString();
        }
        if (min < 10) {
            smin = "0" + min;
        } else {
            smin = min.toString();
        }
        if (hour < 10) {
            shour = "0" + hour;
        } else {
            shour = hour.toString();
        }
        este.jLabel4.setText(shour + ":" + smin + ":" + ssec);
    }

